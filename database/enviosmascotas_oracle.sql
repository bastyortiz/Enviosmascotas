CREATE TABLE SEGUIMIENTO_ENVIOS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NUMERO_SEGUIMIENTO VARCHAR2(50) NOT NULL UNIQUE,
    ESTADO VARCHAR2(30) NOT NULL,
    UBICACION_ACTUAL VARCHAR2(100) NOT NULL,
    DESTINATARIO VARCHAR2(100) NOT NULL,
    DETALLE_PRODUCTO VARCHAR2(255) NOT NULL
);

